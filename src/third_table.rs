/// Map the 8 groups whose third-placed team advanced to their R32 slots, per the FIFA table.
pub fn assign_thirds(groups: &[char]) -> Vec<(u32, char)> {
    let mut mask = 0u16;
    for &g in groups {
        mask |= 1 << (g as u8 - b'A');
    }
    let i = TABLE
        .binary_search_by_key(&mask, |&(m, _)| m)
        .expect("third-place group combination not in FIFA Annex C table");
    SLOT_MATCH_IDS
        .iter()
        .zip(TABLE[i].1)
        .map(|(&mid, g)| (mid, g as char))
        .collect()
}
